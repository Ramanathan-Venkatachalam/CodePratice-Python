#Arithmetic Operators
"""
 +	Addition
-	Subtraction
*	Multiplication
/	Division
%	Modulus
**	Exponentiation
//	Floor division
"""
a= 125
b= 120
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b) #to get answer without decimal
print(a % b) #reminder

print(2 ** 3) #power 2power3 = 2x2x2



